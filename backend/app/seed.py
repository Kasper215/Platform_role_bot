def clear_characters():
    """Удаляет старых персонажей (опционально)."""
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    c.execute("DELETE FROM characters")
    conn.commit()
    conn.close()
    print("🗑️  Старые персонажи удалены.")

def seed_new_characters(characters):
    """Загружает новых персонажей."""
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    
    inserted = 0
    for char in characters:
        try:
            c.execute("""
                INSERT INTO characters (
                    owner_id, name, persona, description, greeting,
                    personality, backstory, motivation, speech_style, catchphrase,
                    scene_title, scene_description, scene_setting,
                    age, gender, race, occupation,
                    traits, likes, fears,
                    is_public, is_nsfw, is_featured,
                    created_at, updated_at
                ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """, (
                1,  # owner_id (admin)
                char["name"],
                char["persona"],
                char["description"],
                char["greeting"],
                char["personality"],
                char["backstory"],
                char["motivation"],
                char["speech_style"],
                char["catchphrase"],
                char["scene_title"],
                char["scene_description"],
                char["scene_setting"],
                char["age"],
                char["gender"],
                char["race"],
                char["occupation"],
                json.dumps(char["traits"]),
                json.dumps(char["likes"]),
                json.dumps(char["fears"]),
                True,  # is_public
                char["is_nsfw"],
                False,  # is_featured
                datetime.now(timezone.utc).isoformat(),
                datetime.now(timezone.utc).isoformat()
            ))
            inserted += 1
        except Exception as e:
            print(f"❌ Ошибка при вставке {char['name']}: {e}")
    
    conn.commit()
    conn.close()
    print(f"✅ Загружено {inserted} новых персонажей.")

