    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    # Считываем реальную схему колонок таблицы characters
    try:
        cursor.execute("PRAGMA table_info(characters);")
        columns_info: List[Any] = cursor.fetchall()
        columns: List[str] = [str(col[1]) for col in columns_info]
        print(f"[Seeder] Detected columns in your DB: {columns}")
    except Exception as e:
        print(f"[Seeder] Failed to detect table columns: {e}")
        conn.close()
        return

    # Очищаем таблицу перед наполнением
    try:
        cursor.execute("DELETE FROM characters;")
        conn.commit()
        print("[Seeder] Cleared old character entries.")
    except Exception as e:
        print(f"[Seeder] Failed to clear table: {e}")
        conn.close()
        return

    owner_id: int = 1
    try:
        cursor.execute("SELECT id FROM users LIMIT 1;")
        user_row = cursor.fetchone()
        if user_row:
            owner_id = int(user_row[0])
            print(f"[Seeder] Found existing user ID {owner_id} to set as owner_id.")
    except Exception as e:
        print(f"[Seeder] Defaulting owner_id to 1. Error: {e}")

    # timezone-aware UTC datetime
    now_str: str = datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M:%S')

